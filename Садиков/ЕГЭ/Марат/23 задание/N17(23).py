#переводит число x в число 2x + 1. Например, вторая команда переводит число 10 в число
#20, а третья переводит число 10 в число 21. Программа для исполнителя Flash25 – это
#последовательность команд. Сколько существует программ, которые число 2 преобразуют в число 16?
def f(a, b):
    if a > b:
        return 0
    if a == b:
        return 1
    return f(a + 1, b) + f(a * 2, b) + f(a * 2 + 1, b)
print(f(2, 16))